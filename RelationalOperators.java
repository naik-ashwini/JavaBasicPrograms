class RelationalOperators
{
	public static void main(String args[])
	{
		int x = 10;
		int y = 20;
		
	    //x == y
		System.out.println(x == y); 		//false
		
		//x != y
		System.out.println(x != y); 		//true
		
		//x < y
		System.out.println(x < y);        //true
		
		//x > y
		System.out.println(x > y);        //false
		
		//x <= y
		System.out.println(x <= y);      //true
		
		//x >= y
		System.out.println(x >= y);      //false
	}
}