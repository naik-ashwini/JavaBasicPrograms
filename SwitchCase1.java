class SwitchCase1
{
	public static void main(String args[])
	{
		//Byte b = 20;
		//byte b = 40;
		//int b = 10;
		//Integer b = 30;
		//Short b = 20;
		//short b = 10;
		//char b = 40;
		Character b = 30;
		
		
		switch(b)
		{
			case 10:
			System.out.println("Ten");
			break;
			
			case 20:
			System.out.println("Twenty");
			break;
			
			case 30:
			System.out.println("Thirty");
			break;
			
			case 40:
			System.out.println("Fourty");
			break;
		}
	}
}